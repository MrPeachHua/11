package com.boxiang.share.product.advertising.po;

import java.io.Serializable;
import java.util.Date;

public class Advertising implements Serializable {
	private static final long serialVersionUID = 1L;

	/** 广告ID */
	private java.lang.Integer id;

	/** 广告名称 */
	private java.lang.String name;

	/** 图片路径 */
	private java.lang.String imagePath;

	/** 图片链接 */
	private java.lang.String imageLink;

	/** 是否可用 */
	private java.lang.String isUsed;
	
	/**	创建时间 */
	private Date createDate;

	public java.lang.String getImageLink() {
		return imageLink;
	}

	public void setImageLink(java.lang.String imageLink) {
		this.imageLink = imageLink;
	}

	public Date getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}

	public java.lang.Integer getId() {
		return id;
	}

	public void setId(java.lang.Integer id) {
		this.id = id;
	}

	public java.lang.String getName() {
		return name;
	}

	public void setName(java.lang.String name) {
		this.name = name;
	}

	public void setImagePath(java.lang.String imagePath) {
		this.imagePath = imagePath;
	}
	
	public java.lang.String getImagePath() {
		return this.imagePath;
	}

	public void setIsUsed(java.lang.String isUsed) {
		this.isUsed = isUsed;
	}
	
	public java.lang.String getIsUsed() {
		return this.isUsed;
	}

}