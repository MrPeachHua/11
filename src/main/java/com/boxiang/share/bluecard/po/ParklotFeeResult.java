package com.boxiang.share.bluecard.po;

/**
 * 停车场算费返回数据内容
 * 
 * @author tanghaibo
 *
 */
public class ParklotFeeResult {

	/**
	 * 状态接收成功：success ；接收失败：fail
	 */
	private String status;

	/**
	 * 失败原因状态为：success 的时候，失败原因为null
	 */
	private String errorCode;

	/**
	 * 车辆停放费用信息车辆停放费用信息
	 */
	private ParklotFeeData datas;

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getErrorCode() {
		return errorCode;
	}

	public void setErrorCode(String errorCode) {
		this.errorCode = errorCode;
	}

	public ParklotFeeData getDatas() {
		return datas;
	}

	public void setDatas(ParklotFeeData datas) {
		this.datas = datas;
	}

	@Override
	public String toString() {
		return "ParklotFeeResult [\"status\":\"" + status + "\", \"errorCod\":\"" + errorCode + "\",\"datas\":" + datas
				+ "]";
	}

}
