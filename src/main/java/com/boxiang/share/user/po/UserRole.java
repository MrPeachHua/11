package com.boxiang.share.user.po;

import java.io.Serializable;

public class UserRole implements Serializable {
	private static final long serialVersionUID = 1L;

	/** 角色编号 */
	private java.lang.Integer roleId;

	/** 角色名称 */
	private java.lang.String roleName;

	/** 角色权限 */
	private java.lang.Integer rolePower;

	/** 是否有效 */
	private java.lang.String isUsed;

	public void setRoleId(java.lang.Integer roleId) {
		this.roleId = roleId;
	}
	
	public java.lang.Integer getRoleId() {
		return this.roleId;
	}

	public void setRoleName(java.lang.String roleName) {
		this.roleName = roleName;
	}
	
	public java.lang.String getRoleName() {
		return this.roleName;
	}

	public void setRolePower(java.lang.Integer rolePower) {
		this.rolePower = rolePower;
	}
	
	public java.lang.Integer getRolePower() {
		return this.rolePower;
	}

	public void setIsUsed(java.lang.String isUsed) {
		this.isUsed = isUsed;
	}
	
	public java.lang.String getIsUsed() {
		return this.isUsed;
	}

}